public class q2 {
    public static void main(String[] args) {
        int num = 2;  // Start from the first even number

        // While loop to print even numbers from 1 to 100
        while (num <= 100) {
            System.out.println(num);
            num += 2;  // Increment by 2 to get the next even number
        }
    }
}
